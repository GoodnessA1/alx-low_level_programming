#include "3-calc.h"

/**
 * op_add - Adds number
 * @a: param 1
 * @b: param 2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function for subtraction
 * @a: param 1
 * @b: param 2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a and b
 * @a: param 1
 * @b: param 2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a and b
 * @a: param 1
 * @b: param 2
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modular operation
 * @a: param
 * @b: param
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
